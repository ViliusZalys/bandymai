package lt.vtvpmc.ems.akademijait.Vilius;

public class Pratybos3 {
    public static char changeStringToCharacterArray (String text){
        for (int charSymbol = 0; charSymbol < text.length(); charSymbol++){
            char c = text.charAt(1);
            return c;
        }
        return 0;
    }
}
